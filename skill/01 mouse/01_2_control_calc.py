buttons = {
    'c': (110, 160),
    'C': (110, 160),
    '0': (50, 280),
    '1': (30, 260),
    '2': (70, 260),
    '3': (110, 260),
    '4': (30, 230),
    '5': (70, 230),
    '6': (110, 230),
    '7': (30, 195),
    '8': (70, 195),
    '9': (110, 195),
    '+': (140, 290),
    '-': (140, 260),
    '*': (140, 230),
    '/': (140, 295),
    '%': (180, 195),
    '=': (180, 270)
}

for c in 'c123+456*789=':
    x, y = buttons[c]
    pyautogui.click(x, y, duration=0.5)

